    private void agregarInventarioGranularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_agregarInventarioGranularActionPerformed
        // TODO add your handling code here:
        String cod = CodigoProductoGranular.getText().trim().toUpperCase();
        Integer cant = parseCantidad(cantidadProductoGranular.getText());
        if (!cod.matches("PRD-\\d{3}") || cant==null || cant<=0) {
            javax.swing.JOptionPane.showMessageDialog(this, "Código PRD-XXX y cantidad > 0");
            return;
        }
        bloquear(true);
        new javax.swing.SwingWorker<Boolean,Void>(){
            String err=null;
            @Override protected Boolean doInBackground(){
                try { return new Cliente.net.ClientApi().productoAjustar(cod, cant); }
                catch(Exception ex){ err=ex.getMessage(); return false; }
            }
            @Override protected void done(){
                bloquear(false);
                try{
                    if(get()){ javax.swing.JOptionPane.showMessageDialog(VentanaInventario.this,"Stock sumado"); cargarInventario(); }
                    else javax.swing.JOptionPane.showMessageDialog(VentanaInventario.this,"Error: "+err);
                }catch(Exception e){ javax.swing.JOptionPane.showMessageDialog(VentanaInventario.this,"Error inesperado");}
            }
        }.execute();
    }//GEN-LAST:event_agregarInventarioGranularActionPerformed

