    private void EliminacionTotalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EliminacionTotalActionPerformed
        // TODO add your handling code here:
        String cod = CodigoProductoborrar.getText().trim().toUpperCase();
        if (!cod.matches("PRD-\\d{3}")){ javax.swing.JOptionPane.showMessageDialog(this,"Código PRD-XXX"); return; }
        int ok = javax.swing.JOptionPane.showConfirmDialog(this,"¿Eliminar definitivamente "+cod+"?","Confirmar",javax.swing.JOptionPane.YES_NO_OPTION);
        if (ok!=javax.swing.JOptionPane.YES_OPTION) return;

        bloquear(true);
        new javax.swing.SwingWorker<Boolean,Void>(){
            String err=null;
            @Override protected Boolean doInBackground(){
                try { return new Cliente.net.ClientApi().productoEliminar(cod); }
                catch(IllegalStateException ise){ err=ise.getMessage(); return false; }
                catch(Exception ex){ err=ex.getMessage(); return false; }
            }
            @Override protected void done(){
                bloquear(false);
                try{
                    if(get()){ javax.swing.JOptionPane.showMessageDialog(VentanaInventario.this,"Producto eliminado"); cargarInventario(); }
                    else {
                        String msg = "Error: "+err;
                        if (err!=null && err.contains("facturas")) msg = "No se puede eliminar: el producto ya tiene facturas.";
                        javax.swing.JOptionPane.showMessageDialog(VentanaInventario.this, msg);
                    }
                }catch(Exception e){ javax.swing.JOptionPane.showMessageDialog(VentanaInventario.this,"Error inesperado");}
            }
        }.execute();
    }//GEN-LAST:event_EliminacionTotalActionPerformed

