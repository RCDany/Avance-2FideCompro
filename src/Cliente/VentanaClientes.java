    private void cargarClientes() {
        AreaInventario.setText("Cargando...\n");
        new javax.swing.SwingWorker<java.util.List<Cliente.net.ClientApi.ClienteDet>, Void>() {
            String error = null;
            @Override protected java.util.List<Cliente.net.ClientApi.ClienteDet> doInBackground() {
                try { return new Cliente.net.ClientApi().clienteListar(); }
                catch (Exception ex) { error = ex.getMessage(); return java.util.List.of(); }
            }
            @Override protected void done() {
                try {
                    var lista = get();
                    if (error != null) { AreaInventario.setText("Error: " + error); return; }
                    StringBuilder sb = new StringBuilder();
                    for (var c : lista) {
                        sb.append(String.format("%s | %s %s | %s%s%n",
                            c.cedula, c.nombre, c.apellidos, c.correo,
                            c.esJuridica() ? " | " + (c.empresa == null ? "" : c.empresa) : ""));
                    }
                    AreaInventario.setText(sb.toString());
                } catch (Exception e) { AreaInventario.setText("Error inesperado."); }
            }
        }.execute();
    }
    private String formatearCliente(Cliente.net.ClientApi.ClienteDet c){
        return String.format(
            "%s | %s %s | %s%s",
            c.cedula, c.nombre, c.apellidos, c.correo,
            c.esJuridica() ? " | " + (c.empresa == null ? "" : c.empresa) : ""
        );
    }
