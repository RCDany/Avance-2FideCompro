    private void CedulaEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CedulaEditarActionPerformed
        // TODO add your handling code here:
        String ced = CodigoProductoGranular.getText().trim();
            if (!ced.matches("\\d{9,10}")) {
                javax.swing.JOptionPane.showMessageDialog(this, "Cédula inválida (9-10 dígitos).");
                return;
            }
            guardarCambios.setEnabled(false);

            new javax.swing.SwingWorker<Cliente.net.ClientApi.ClienteDet, Void>() {
                String error = null;
                @Override protected Cliente.net.ClientApi.ClienteDet doInBackground() {
                    try {
                        var api = new Cliente.net.ClientApi();
                        return api.clienteGet(ced); 
                    } catch (Exception ex) {
                        error = ex.getMessage();
                        return null;
                    }
                }
                @Override protected void done() {
                    guardarCambios.setEnabled(true);
                    try {
                        var det = get();
                        if (det == null) {
                            javax.swing.JOptionPane.showMessageDialog(
                                VentanaClientes.this,
                                error != null ? "Error: " + error : "Cliente no encontrado."
                            );
                            return;
                        }
                        nombre.setText(det.nombre);
                        apellidos.setText(det.apellidos);
                        email.setText(det.correo);
                        boolean esJuridica = "JURIDICA".equalsIgnoreCase(det.tipoCedula);
                        setEmpresaVisible(esJuridica);
                        empresafield.setText(esJuridica ? (det.empresa == null ? "" : det.empresa) : "");
                        setCamposEdicionEnabled(true);
                        if (!esJuridica) empresafield.setText("");  
                        email.requestFocusInWindow();
                    } catch (Exception e) {
                        javax.swing.JOptionPane.showMessageDialog(VentanaClientes.this, "Error inesperado.");
                    }
                }

            }.execute();
    }//GEN-LAST:event_CedulaEditarActionPerformed

